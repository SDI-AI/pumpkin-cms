import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const root = path.resolve(import.meta.dirname, '..');
const schemaRoot = path.join(root, 'schemas', 'blocks');
const checkOnly = process.argv.includes('--check');
const generatedHeader = '// <auto-generated> Generated from schemas/blocks. Do not edit by hand. </auto-generated>';

const fileNames = (await readdir(schemaRoot))
  .filter((name) => name.endsWith('.schema.json'));
const schemasByFile = new Map(await Promise.all(fileNames.map(async (name) => [
  name,
  JSON.parse(await readFile(path.join(schemaRoot, name), 'utf8')),
])));
const unionSchema = schemasByFile.get('html-block.schema.json');
const blockSchemas = unionSchema.oneOf.map(({ $ref }) => {
  const schema = schemasByFile.get($ref);
  if (!schema?.['x-pumpkin']) throw new Error(`Missing block schema metadata: ${$ref}`);
  return { file: $ref, schema };
});

validateCatalog(blockSchemas);

const outputs = new Map();
for (const [moduleName, entries] of groupBy(blockSchemas, ({ schema }) => schema['x-pumpkin'].module)) {
  outputs.set(
    path.join(root, 'packages', 'pumpkin-ts-models', 'src', 'models', `${moduleName}.ts`),
    renderTypeScriptModule(entries),
  );
}
outputs.set(
  path.join(root, 'packages', 'pumpkin-ts-models', 'src', 'models', 'HtmlBlockTypes.ts'),
  renderTypeScriptUnion(blockSchemas),
);
outputs.set(
  path.join(root, 'apps', 'starter-app', 'src', 'components', 'blocks', 'blockDefaults.generated.ts'),
  renderDefaults(blockSchemas),
);
outputs.set(
  path.join(root, 'apps', 'pumpkin-net-models', 'Models', 'GeneratedBlockContracts.g.cs'),
  renderCSharp(blockSchemas),
);
outputs.set(
  path.join(root, 'apps', 'pumpkin-api.Tests', 'Fixtures', 'block-contracts.generated.json'),
  renderFixtures(blockSchemas),
);

let stale = false;
for (const [file, content] of outputs) {
  const normalized = `${content.trimEnd()}\n`;
  const existing = await readFile(file, 'utf8').catch(() => '');
  if (normalize(existing) === normalize(normalized)) continue;
  stale = true;
  if (checkOnly) {
    console.error(`Generated block contract is stale: ${path.relative(root, file)}`);
  } else {
    await mkdir(path.dirname(file), { recursive: true });
    await writeFile(file, normalized, 'utf8');
    console.log(`Generated ${path.relative(root, file)}`);
  }
}

if (checkOnly && stale) process.exitCode = 1;
if (checkOnly && !stale) console.log(`Block contracts are current (${blockSchemas.length} block types).`);

function validateCatalog(entries) {
  const types = new Set();
  const titles = new Set();
  for (const { file, schema } of entries) {
    const metadata = schema['x-pumpkin'];
    if (types.has(metadata.type)) throw new Error(`Duplicate block type: ${metadata.type}`);
    if (titles.has(schema.title)) throw new Error(`Duplicate block class: ${schema.title}`);
    types.add(metadata.type);
    titles.add(schema.title);
    if (!schema.$defs?.[metadata.contentType]) throw new Error(`${file} does not define ${metadata.contentType}`);
  }
}

function renderTypeScriptModule(entries) {
  const lines = [generatedHeader, '', "import type { IHtmlBlock } from './IHtmlBlock';", ''];
  const sharedTypes = ['ImageAspect', 'ImageFit', 'ImagePosition'].filter((name) =>
    entries.some(({ schema }) => JSON.stringify(schema).includes(`shared.schema.json#/$defs/${name}`)),
  );
  if (sharedTypes.length > 0) {
    lines.push(`import type { ${sharedTypes.join(', ')} } from './ImagePresentation';`, '');
  }
  for (const { schema } of entries) {
    for (const [name, definition] of Object.entries(schema.$defs ?? {})) {
      lines.push(renderTsDefinition(name, definition), '');
    }
    const metadata = schema['x-pumpkin'];
    lines.push(
      `export interface ${schema.title} extends IHtmlBlock {`,
      `  type: ${JSON.stringify(metadata.type)};`,
      `  content: ${metadata.contentType};`,
      '}',
      '',
    );
  }
  return lines.join('\n');
}

function renderTsDefinition(name, definition) {
  if (definition.type !== 'object') {
    return `export type ${name} = ${tsType(definition)};`;
  }
  const required = new Set(definition.required ?? []);
  const properties = Object.entries(definition.properties ?? {}).map(([jsonName, property]) => {
    const key = /^[A-Za-z_$][\w$]*$/.test(jsonName) ? jsonName : JSON.stringify(jsonName);
    return `  ${key}${required.has(jsonName) ? '' : '?'}: ${tsType(property)};`;
  });
  return [`export interface ${name} {`, ...properties, '}'].join('\n');
}

function tsType(schema) {
  const reference = referenceName(schema.$ref);
  if (reference) return reference;
  if (schema.const !== undefined) return JSON.stringify(schema.const);
  if (schema.enum) return schema.enum.map(JSON.stringify).join(' | ');
  if (Array.isArray(schema.type)) return schema.type.map((item) => tsType({ type: item })).join(' | ');
  switch (schema.type) {
    case 'string': return 'string';
    case 'integer':
    case 'number': return 'number';
    case 'boolean': return 'boolean';
    case 'array': return `${tsType(schema.items ?? {})}[]`;
    case 'object': return 'Record<string, unknown>';
    default: return 'unknown';
  }
}

function renderTypeScriptUnion(entries) {
  const imports = entries.map(({ schema }) => (
    `import type { ${schema.title} } from './${schema['x-pumpkin'].module}';`
  ));
  const titles = entries.map(({ schema }) => schema.title);
  const mapLines = entries.map(({ schema }) => (
    `  ${JSON.stringify(schema['x-pumpkin'].type)}: ${JSON.stringify(schema.title)},`
  ));
  return [
    generatedHeader,
    '',
    "import type { IHtmlBlock } from './IHtmlBlock';",
    ...imports,
    '',
    'export type HtmlBlock =',
    ...titles.map((title, index) => `  ${index === 0 ? '' : '|'} ${title}`),
    ';',
    '',
    'export const BLOCK_TYPE_MAP = {',
    ...mapLines,
    '} as const;',
    '',
    'export const SUPPORTED_BLOCK_TYPES = Object.keys(BLOCK_TYPE_MAP) as Array<keyof typeof BLOCK_TYPE_MAP>;',
    '',
    "export function isBlockOfType<T extends HtmlBlock['type']>(",
    '  block: IHtmlBlock,',
    '  type: T,',
    "): block is Extract<HtmlBlock, { type: T }> {",
    '  return block.type === type;',
    '}',
    '',
    'export function isHtmlBlock(obj: unknown): obj is IHtmlBlock {',
    "  if (!obj || typeof obj !== 'object') return false;",
    "  const candidate = obj as { type?: unknown; content?: unknown };",
    "  return typeof candidate.type === 'string' && !!candidate.content && typeof candidate.content === 'object';",
    '}',
    '',
    'export function createGenericBlock(type: string, content: Record<string, unknown>): IHtmlBlock {',
    '  return { type, content };',
    '}',
  ].join('\n');
}

function renderDefaults(entries) {
  const lines = [generatedHeader, '', "import type { IHtmlBlock } from 'pumpkin-ts-models';", '', 'export const GENERATED_BLOCK_DEFAULT_CONTENT = {'];
  for (const { schema } of entries) {
    const metadata = schema['x-pumpkin'];
    lines.push(`  ${JSON.stringify(metadata.type)}: ${JSON.stringify(defaultValue(schema.$defs[metadata.contentType], schema, true))},`);
  }
  lines.push(
    '} as const;',
    '',
    'export function createGeneratedDefaultBlock(type: string): IHtmlBlock {',
    '  const content = GENERATED_BLOCK_DEFAULT_CONTENT[type as keyof typeof GENERATED_BLOCK_DEFAULT_CONTENT] ?? {};',
    '  return { type, schemaVersion: 1, content: structuredClone(content) } as IHtmlBlock;',
    '}',
  );
  return lines.join('\n');
}

function renderFixtures(entries) {
  const fixtures = entries.map(({ schema }) => {
    const metadata = schema['x-pumpkin'];
    return {
      id: `fixture-${metadata.type.toLowerCase()}`,
      name: `${metadata.type} fixture`,
      enabled: false,
      schemaVersion: 1,
      type: metadata.type,
      content: defaultValue(schema.$defs[metadata.contentType], schema, true),
    };
  });
  return JSON.stringify(fixtures, null, 2);
}

function defaultValue(definition, ownerSchema, includeOptional = false) {
  const resolved = resolveDefinition(definition, ownerSchema);
  if (resolved.default !== undefined) return resolved.default;
  if (resolved.type === 'object') {
    const required = new Set(resolved.required ?? []);
    return Object.fromEntries(Object.entries(resolved.properties ?? {})
      .filter(([name, property]) => required.has(name) || includeOptional || resolveDefinition(property, ownerSchema).default !== undefined)
      .map(([name, property]) => [name, defaultValue(property, ownerSchema, includeOptional)]));
  }
  if (resolved.type === 'array') return [];
  if (resolved.type === 'boolean') return false;
  if (resolved.type === 'integer' || resolved.type === 'number') return 0;
  return '';
}

function renderCSharp(entries) {
  const lines = [
    '// <auto-generated> Generated from schemas/blocks. Do not edit by hand. </auto-generated>',
    '#nullable enable',
    'using System.Text.Json;',
    'using System.Text.Json.Serialization;',
    '',
    'namespace pumpkin_net_models.Models;',
    '',
  ];
  const emitted = new Set();
  for (const { schema } of entries) {
    const metadata = schema['x-pumpkin'];
    lines.push(
      `public class ${schema.title} : HtmlBlockBase`,
      '{',
      '    [JsonPropertyName("type")]',
      `    public override string Type { get; set; } = ${csharpString(metadata.type)};`,
      '',
      '    [JsonPropertyName("content")]',
      `    public override object Content { get; set; } = new ${metadata.contentType}();`,
      '}',
      '',
    );
    for (const [name, definition] of Object.entries(schema.$defs ?? {})) {
      if (definition.type !== 'object' || emitted.has(name)) continue;
      emitted.add(name);
      lines.push(renderCSharpClass(name, definition, schema), '');
    }
  }
  lines.push(
    'internal static class GeneratedBlockTypeRegistry',
    '{',
    '    internal static readonly IReadOnlyDictionary<string, Type> Types = new Dictionary<string, Type>(StringComparer.Ordinal)',
    '    {',
    ...entries.map(({ schema }) => `        [${csharpString(schema['x-pumpkin'].type)}] = typeof(${schema.title}),`),
    '    };',
    '}',
    '',
    'public static class HtmlBlockContractValidator',
    '{',
    '    private static readonly IReadOnlyDictionary<string, string[]> RequiredContentProperties =',
    '        new Dictionary<string, string[]>(StringComparer.Ordinal)',
    '        {',
    ...entries.map(({ schema }) => {
      const metadata = schema['x-pumpkin'];
      const required = schema.$defs[metadata.contentType].required ?? [];
      return `            [${csharpString(metadata.type)}] = new[] { ${required.map(csharpString).join(', ')} },`;
    }),
    '        };',
    '',
    '    public static IReadOnlyList<string> ValidatePage(Page page)',
    '    {',
    '        var errors = new List<string>();',
    '        for (var index = 0; index < page.ContentData.ContentBlocks.Count; index++)',
    '        {',
    '            var block = page.ContentData.ContentBlocks[index];',
    '            if (!RequiredContentProperties.TryGetValue(block.Type, out var required)) continue;',
    '',
    '            if (block.Content is null)',
    '            {',
    '                errors.Add($"ContentData.ContentBlocks[{index}].content must be an object.");',
    '                continue;',
    '            }',
    '',
    '            var content = block.Content is JsonElement element',
    '                ? element',
    '                : JsonSerializer.SerializeToElement(block.Content, block.Content.GetType());',
    '            if (content.ValueKind != JsonValueKind.Object)',
    '            {',
    '                errors.Add($"ContentData.ContentBlocks[{index}].content must be an object.");',
    '                continue;',
    '            }',
    '',
    '            foreach (var property in required)',
    '            {',
    '                if (!content.TryGetProperty(property, out _))',
    '                    errors.Add($"ContentData.ContentBlocks[{index}].content.{property} is required for {block.Type}.");',
    '            }',
    '        }',
    '        return errors;',
    '    }',
    '}',
  );
  return lines.join('\n');
}

function renderCSharpClass(name, definition, ownerSchema) {
  const required = new Set(definition.required ?? []);
  const lines = [`public class ${name}`, '{'];
  for (const [jsonName, property] of Object.entries(definition.properties ?? {})) {
    const isRequired = required.has(jsonName);
    const propertyName = property['x-csharp-name'] ?? pascalCase(jsonName);
    const resolved = resolveDefinition(property, ownerSchema);
    const type = csharpType(property, ownerSchema, isRequired);
    lines.push(`    [JsonPropertyName(${csharpString(jsonName)})]`);
    lines.push(`    public ${type} ${propertyName} { get; set; }${csharpInitializer(property, ownerSchema, isRequired)}`);
    lines.push('');
  }
  if (lines.at(-1) === '') lines.pop();
  lines.push('}');
  return lines.join('\n');
}

function csharpType(schema, ownerSchema, required) {
  const reference = referenceName(schema.$ref);
  const resolved = resolveDefinition(schema, ownerSchema);
  if (resolved.type === 'object' && reference) return `${reference}${required ? '' : '?'}`;
  switch (resolved.type) {
    case 'string': return `string${required ? '' : '?'}`;
    case 'integer': return `int${required ? '' : '?'}`;
    case 'number': return `double${required ? '' : '?'}`;
    case 'boolean': return `bool${required ? '' : '?'}`;
    case 'array': return `List<${csharpArrayItemType(resolved.items, ownerSchema)}>`;
    case 'object': return `${reference ?? 'Dictionary<string, object?>'}${required ? '' : '?'}`;
    default: return 'object?';
  }
}

function csharpArrayItemType(schema, ownerSchema) {
  const reference = referenceName(schema?.$ref);
  if (reference) return reference;
  const resolved = resolveDefinition(schema ?? {}, ownerSchema);
  if (resolved.type === 'string') return 'string';
  if (resolved.type === 'integer') return 'int';
  if (resolved.type === 'number') return 'double';
  if (resolved.type === 'boolean') return 'bool';
  return 'object';
}

function csharpInitializer(schema, ownerSchema, required) {
  const resolved = resolveDefinition(schema, ownerSchema);
  if (resolved.type === 'array') return ' = new();';
  if (!required && resolved.default === undefined) return '';
  const value = defaultValue(schema, ownerSchema);
  if (typeof value === 'string') return ` = ${value ? csharpString(value) : 'string.Empty'};`;
  if (typeof value === 'boolean') return ` = ${value ? 'true' : 'false'};`;
  if (typeof value === 'number') return ` = ${value};`;
  const reference = referenceName(schema.$ref);
  if (resolved.type === 'object' && reference) return ` = new ${reference}();`;
  return '';
}

function resolveDefinition(schema, ownerSchema) {
  if (!schema?.$ref) return schema ?? {};
  const [filePart, pointer] = schema.$ref.split('#');
  const targetSchema = filePart ? schemasByFile.get(filePart) : ownerSchema;
  if (!targetSchema) throw new Error(`Unable to resolve schema reference: ${schema.$ref}`);
  if (!pointer) return targetSchema;
  return pointer.split('/').filter(Boolean).reduce((value, key) => value[key.replaceAll('~1', '/').replaceAll('~0', '~')], targetSchema);
}

function referenceName(reference) {
  return reference?.match(/#\/\$defs\/([^/]+)$/)?.[1];
}

function pascalCase(value) {
  return value.split(/[^A-Za-z0-9]+/).filter(Boolean).map((part) => part[0].toUpperCase() + part.slice(1)).join('');
}

function csharpString(value) {
  return `"${String(value).replaceAll('\\', '\\\\').replaceAll('"', '\\"')}"`;
}

function groupBy(items, selector) {
  const groups = new Map();
  for (const item of items) {
    const key = selector(item);
    groups.set(key, [...(groups.get(key) ?? []), item]);
  }
  return groups;
}

function normalize(value) {
  return value.replaceAll('\r\n', '\n');
}
